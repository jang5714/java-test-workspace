package com.example.demo.util;

public class MyCalculator {
    public int plus(int a, int b){
       return a+b;
    }
    public int minus(int a, int b){
        return a-b;
}
    public int multi(int a, int b){
        return a*b;
    }
    public int division(int a, int b){
        return a/b;
    }
}
